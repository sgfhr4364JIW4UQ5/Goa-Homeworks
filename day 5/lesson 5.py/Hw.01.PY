#ჯოტარო კუჯო
#დაიბადა 1970 წელს იაპონიაში
#მისი მშობლები იყვნენ ჰოლი კუჯო იგივე დედა და სადაო კუჯო მამა
#დედამისი იყო ჩვეულებრივი ადამიანი რომელიც იყო დიასახლისი და მამამისი მუშაუბდა ჯარში და ის პილოტი იყო 
#როცა ის იყო 17 წლის ის თავისით ჩაჯდა ციხეში რადგან ამბობდა რაღაც  სული დაყვებოდა იქამდე სანამ ბაბუამისი არ მოვიდა და აუხსნა ტუ რა იყო ეს სული
#როცა სახში დაბრუნდნენ ნახეს რომ დედამისი ავად იყო და მათ უკვე იცოდნენ თუ საიდან მოდიადა ეს წყველა და ვისგან
#მათ გადაწყვიტეს დედამისის დახმარება და ისინი წავიდნენ ეგვიპტეში სანამ იქ ჩავიდოდნენ მათ გაიარეს რამდენიმე ქვეყანა და შეიძინეს ახალი მეგობრები
#დიდი ხნის შემდეგ როცა ჩავიდენ დიომ უკვე 3 მათგანი მოკლა
#ამ ყველაფრის შემდეგ ისინი ერთმანეთს შეხვდნენ და დღეები ბრძოლობდნენ და ჯოტარომ ის დაამარცხა მმაგრამ ბაბუამისი დაკარგა და თავის ძალის დახმარებიტ ის დააბრუნა
